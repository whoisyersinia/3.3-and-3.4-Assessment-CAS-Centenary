						<p>Christchurch Adventist School was officially opened on April 27, 1925, at 15 Grants Road, Papanui, Christchurch. It started with an enrollment of 22 students. Maud Smart was their teacher and remained in charge with a handful of assistants until 1933. Statistics for 1934 mention three teachers caring for 43 students from grades 1 through 10.
						<p>Smart returned as principal in 1941, but spent some of 1943 recovering from a minor accident. Donald Watson, while waiting for a ship to his Pitcairn Island mission field, taught during her absence. At the time the institution was referred to as the Christchurch Central School. By 1954 the enrollment had risen significantly, requiring three teachers for the elementary levels and four teachers for the high school levels.</p>
						<p>The last time Smart appeared as principal was 1956, but she continued as a teacher until her retirement in 1958. Her brief successor as principal was Hardy Dawson, followed by an equally brief term by Harold Heath. Wilton Zanotti served from 1959 through 1963.The next principal, Richard Thompson, in cooperation with the Parent-Teacher Association, worked to install a swimming pool that was officially opened on December 4, 1966. Subsequently, Stanley Gillis, Hedley Eager, Keith Dickens, and Peter Truscott served as principals. In the 1970s it had matured to the point where it offered all levels of elementary and on to the fourth year of high school.</p>

						<p>In the 1990s the institution was renamed the Christchurch Adventist High School and had shrunk to the second year of high school, but has since revived to offer all high school levels. Since 2006 its name has been further revised to Christchurch Adventist School. It remains a coeducational institution. Those who have served as principals during the past three decades, 1998 though 2018, are Elwin King-Adams, Verle Thompson, Mark Hansen, Danny Carrasco, Cherie Galloway and Evan Ellis.</p>
